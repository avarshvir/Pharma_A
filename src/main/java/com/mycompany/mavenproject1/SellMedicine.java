    private void searchMedicineTableMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_searchMedicineTableMouseClicked
        // TODO add your handling code here:
        DefaultTableModel dfm = (DefaultTableModel)searchMedicineTable.getModel();
        int seletedRow = searchMedicineTable.getSelectedRow();
        String id = dfm.getValueAt(seletedRow, 0).toString();
        try {
                System.out.println("in condition");
                String selectStatement = "SELECT * FROM addmedicine WHERE id = ?";
                PreparedStatement preparedStatement = singletonClass.connection.prepareStatement(selectStatement);
                preparedStatement.setString(1, id);
                ResultSet rs = preparedStatement.executeQuery();
                

                while (rs.next()) {

                    selectedMedicine.setId(Integer.parseInt(String.valueOf(rs.getInt(1))));
                    selectedMedicine.setCompany(rs.getString("company"));
                    selectedMedicine.setExpiryDate(rs.getString("expiryDate"));
                    selectedMedicine.setGenre(rs.getString("genre"));
                    selectedMedicine.setMedicineId(rs.getString("medicineId"));
                    selectedMedicine.setMfdDate(rs.getString("mfgDate"));
                    selectedMedicine.setMedicineName(rs.getString("medicineName"));
                    selectedMedicine.setQuantity(Integer.parseInt(rs.getString("quantity")));
                    selectedMedicine.setPricePerUnit(Double.parseDouble(rs.getString("pricePerUnit")));
                    selectedMedicine.setSellingUnit(1);
                    updateMedicineInfo();
                }
               
            } catch (SQLException sqlException) {
                System.out.println("in exception 631" + sqlException.getMessage());
            }
    }//GEN-LAST:event_searchMedicineTableMouseClicked

