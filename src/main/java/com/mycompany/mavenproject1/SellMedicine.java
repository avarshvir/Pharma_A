    private void addToCartMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_addToCartMouseClicked
        // TODO add your handling code here:
        if (name.getText().isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "Enter name");
        } else if (customerPhone.getText().isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "enter your id");
        } else if (medicineName.getText().isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "enter Company Name");
        } else if (availableUnits.getText().isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "enter Number of Units");
        } else if (pricePerUnit.getText().isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "enter Price Per Units");
        } else if (totalPrice.getText().isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "enter Total Price");
        } else {
            String insertInDB = "SELECT * FROM addMedicine where id = ?";

            try {
                PreparedStatement prepareStatement = singletonClass.connection.prepareCall(insertInDB);
                
                prepareStatement.setInt(1 ,selectedMedicine.getId());
                ResultSet rs = prepareStatement.executeQuery();
                MedicineModel addtoCartModel = new MedicineModel();
                  while (rs.next()) {
                    addtoCartModel.setId(Integer.parseInt(String.valueOf(rs.getInt(1))));
                    addtoCartModel.setCompany(rs.getString("company"));
                    addtoCartModel.setExpiryDate(rs.getString("expiryDate"));
                    addtoCartModel.setGenre(rs.getString("genre"));
                    addtoCartModel.setMedicineId(rs.getString("medicineId"));
                    addtoCartModel.setMfdDate(rs.getString("mfgDate"));
                    addtoCartModel.setMedicineName(rs.getString("medicineName"));
                    addtoCartModel.setQuantity(Integer.parseInt(rs.getString("quantity")));
                    addtoCartModel.setPricePerUnit(Double.parseDouble(pricePerUnit.getText().toString()));
                    addtoCartModel.setSellingUnit(Integer.valueOf(sellUnits.getText().toString()));
                    
                    medicineArray.add(addtoCartModel);
                    clearView();
                    updateCart();
                }

            } catch (SQLException sqlException) {
                System.out.println("in exception array " + sqlException.getMessage());

            }
        }
    }//GEN-LAST:event_addToCartMouseClicked

